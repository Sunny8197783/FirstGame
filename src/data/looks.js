function pickLook(ctype) {
  const pool = CUSTOMER_LOOKS[ctype.type];
  if (!pool || !pool.length) return { face: ctype.emoji, desc: '', n: 0 };
  const i = randInt(0, pool.length - 1);
  return { ...pool[i], n: i + 1 };
}

Object.assign(globalThis, { CUSTOMER_LOOKS, pickLook });
